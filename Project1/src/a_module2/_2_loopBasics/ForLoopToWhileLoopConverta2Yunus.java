package a_module2._2_loopBasics;

public class ForLoopToWhileLoopConverta2Yunus {

	public static void main(String[] args) {
		System.out.println("a.");
		int max = 5;
		for (int n = 1; n <= max; n++) {
		    System.out.println(n);

		}
		System.out.println();
		
		int num = 1;
		while(num <= 5) {
			System.out.println(num);
			num++;
		}

	}

}
